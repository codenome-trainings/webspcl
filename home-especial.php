    <section id="AcessibilidadeWeb">
        <h1>Acessibilidade web</h1>
        <p>A <strong>expressão acessibilidade</strong> Web refere-se a prática inclusiva de fazer
            websites
            que possam ser
            utilizados por todas as pessoas que tenham deficiência ou não. Quando os sites são
            corretamente
            concebidos, desenvolvidos e editados, todos os usuários podem ter igual acesso à informação
            e
            funcionalidade.</p>
        <p>Por exemplo, quando um site é desenvolvido utilizando corretamente as semânticas HTML,
            acrescentando a descrição textual alternativa equivalente ao conteúdo das imagens e com
            links
            nomeados de forma significativa, esta ação ajuda usuários cegos ou com deficiência visual a
            obterem auxílio com o uso de softwares Leitores de Textos e/ou com hardware específico para
            transformação do texto em Braille.</p>
        <p>Quando o texto e as imagens são grandes e/ou passíveis de ampliação a leitura e compreensão
            do
            conteúdo torna-se mais fácil para os usuários com visão deficiente. Quando os links são
            sublinhados (ou diferenciados de outra maneira) e também coloridos, tem-se a garantia de que
            usuários daltônicos sejam capazes de diferenciá-los. Quando os links e áreas clicáveis são
            grandes isso ajuda os usuários que não podem controlar um mouse com precisão. Quando as
            páginas
            são codificadas para os usuários poderem navegar utilizando somente o teclado ou uma única
            tecla
            de acesso ajuda os usuários que não pode utilizar um mouse ou até mesmo um teclado padrão.
            Quando vídeos são legendados ou são disponibilizados com versões em linguagem de sinais, os
            usuários surdos ou com dificuldade de audição podem entender o vídeo. Quando são evitados ou
            tornados opcionais os efeitos de "pisca-pisca" ou efeitos luminosos estroboscópicos os
            usuários
            propensos a fotoconvulsões não são colocados em risco. E quando o conteúdo é escrito em
            linguagem simples e ilustrado com gráficos e animações instrucionais os usuários com
            dislexia e
            dificuldades de aprendizagem são capazes de compreender melhor o conteúdo. Quando os sites
            são
            corretamente construídos e mantidos, as necessidades de todos esses usuários podem ser
            acomodadas, sem causar impacto na usabilidade do site para os demais usuários.</p>
        <p>As necessidades que a "acessibilidade web" pretende abordar incluem:</p>
        <ul>
            <li><strong>Visual:</strong>
                <p>Deficiências visuais, incluindo cegueira, vários tipos comuns de
                    baixa visão e baixa acuidade visual, vários tipos de daltonismo;</p>
            </li>
            <li><strong>Motora / Mobilidade:</strong>
                <p>por exemplo, dificuldade ou impossibilidade de
                    utilizar as mãos, incluindo tremores, lentidão muscular, perda ou baixo controle
                    muscular, entre outras, devidas a condições tais como doença de Parkinson,
                    distrofia muscular, paralisia cerebral, acidente vascular cerebral;</p>
            </li>
            <li><strong>Auditivos:</strong>
                <p>Surdez ou deficiência auditiva, incluindo indivíduos com pouca audição;</p>
            </li>
            <li><strong>Convulsões:</strong>
                <p>convulsão visual causada pelos efeitos estroboscópicos ou pisca-pisca.</p>
            </li>
            <li><strong>Cognitiva / Intelectual: </strong>
                <p>Deficiência de desenvolvimento, dificuldades de
                    aprendizagem dislexia, discalculia, entre outras), e deficiências cognitivas de
                    várias
                    origens, afetando
                    a memória, a atenção, o desenvolvimento (maturidade) as habilidades lógicas e de
                    resolução de problemas,
                    entre outras;</p>
            </li>
        </ul>
