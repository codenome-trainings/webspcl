            <p>O Virtual Vision é a solução definitiva para que pessoas com deficiência visual
                possam
                utilizar com autonomia o Windows, o Office, o Internet Explorer e outros
                aplicativos,
                através da leitura dos menus e telas desses programas por um sintetizador de
                voz.</p>
            <p>O Virtual Vision "varre" os programas em busca de informações que podem ser lidas
                para o
                usuário, possibilitando a navegação por menus, telas e textos presentes em
                praticamente
                qualquer aplicativo. </p>
            <p>A navegação é realizada por meio de um teclado comum e o som é emitido através da
                placa
                de som presente no computador. Nenhuma adaptação especial é necessária para que o
                programa funcione e possibilite a utilização do computador pelas pessoas com
                deficiência
