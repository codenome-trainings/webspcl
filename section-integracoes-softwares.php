    <h3>Headmouse e Teclado Virtual</h3>
    <p>O Headmouse e o Teclado Virtual são tecnologias inovadoras que permitem a portadores de
        deficiência física acesso facilitado à internet e ao uso de computadores pessoais. As duas
        aplicações podem ser instaladas em qualquer computador equipado com webcam de baixo custo.
        As
        ferramentas foram desenvolvidas pela empresa espanhola Indra, multinacional de tecnologia da
        informação com larga experiência na Europa e América Latina, em conjunto com a Fundação
        Adecco e
        a Universidade de Lleida, na Espanha. Os Correios também terão oportunamente uma equipe de
        suporte treinada pela Indra para esclarecer dúvidas relativas à instalação e ao
        funcionamento
        das duas ferramentas. O Headmouse é uma solução tecnológica que permite às pessoas com
        mobilidade reduzida controlar o cursor do mouse pelos movimentos da cabeça. O software
        interpreta funções como “arrastar” arquivos por gestos faciais e piscar de olhos.
        Complementando
        a aplicação, o Teclado Virtual facilita às pessoas com deficiência física a possibilidade de
        redação de textos sem a necessidade de utilizar as mãos, já que capta os movimentos faciais
        do
        usuário, replicando-os sobre o um teclado digital.</p>
